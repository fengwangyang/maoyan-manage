        showOnhot:function(page,rows,type,value){
            let obj={};
            if(type){
                obj[type]=value;
            }
            obj.page = page,
            obj.rows = rows,
            ajax({
                type:'get',
                url:'/hotshowing/find',
                data:obj,
                success:(data)=>{
                    store.commit("ONHOT_DATA",data);
